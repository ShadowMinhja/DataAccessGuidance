param(
    [Parameter(Mandatory=$true)][string] $sqlConnectionString,
    [Parameter(Mandatory=$true)][string] $mongoConnectionString
)

Write-Host "Extracting products from SQL Server to MongoDB..." -ForegroundColor Yellow

$scriptPath = Split-Path -parent $MyInvocation.MyCommand.Definition
$mongoLibPath = Join-Path $scriptPath "..\.\packages\mongocsharpdriver.1.8.1\lib\net35\MongoDB.Driver.dll"
#$mongoConnectionString = "mongodb://127.0.0.1"
$mongoCollectionName = "products"

$batchSize = 100
$numberToSkip = 0

Add-Type -Path $mongoLibPath
$client = New-Object MongoDB.Driver.MongoClient($mongoConnectionString)
$server = $client.GetServer()
$database = $server.GetDatabase("eCommerce")

if ($database.CollectionExists($mongoCollectionName))
{
    Write-Host ("Collection " + $mongoCollectionName + " exists.  Dropping...") -ForegroundColor Yellow
    $commandResult = $database.DropCollection($mongoCollectionName)
    if (!$commandResult.Ok)
    {
        Write-Error ("Failed to drop " + $mongoCollectionName + " collection.")
        exit
    }
    Write-Host "Collection dropped." -ForegroundColor Green
}

#$sqlConnectionString = "Data Source=(local);Initial Catalog=eCommerce; Integrated Security=SSPI"
$sqlCommandText = "SELECT p.[ProductID] as ProductId, pc.[ProductCategoryID] as ProductCategoryId, pc.[Name] as ProductCategoryName, 
                          ps.[ProductSubcategoryID] as ProductSubCategoryId, ps.[Name] as ProductSubCategoryName,
                          p.[Name], p.[ProductNumber], p.[Color], p.[ListPrice], p.[Size],
                          RTRIM(p.[SizeUnitMeasureCode]) as SizeUnitMeasureCode, RTRIM(p.[WeightUnitMeasureCode]) as WeightUnitMeasureCode,
                          p.[Weight], RTRIM(p.[Class]) as Class, RTRIM(p.[Style]) as Style
                   FROM [eCommerce].[Production].[Product] p 
                   INNER JOIN [eCommerce].[Production].[ProductSubcategory] ps on p.ProductSubcategoryID = ps.ProductSubcategoryID 
                   INNER JOIN [eCommerce].[Production].[ProductCategory] pc on ps.ProductCategoryID = pc.ProductCategoryID
                   ORDER BY p.[ProductID] OFFSET @numberToSkip ROWS FETCH NEXT @batchSize ROWS ONLY"

$sqlConnection = New-Object System.Data.SqlClient.SqlConnection
$sqlConnection.ConnectionString = $sqlConnectionString
$selectCmd = $sqlConnection.CreateCommand()
$selectCmd.CommandType = [System.Data.CommandType]::Text
$selectCmd.CommandText = $sqlCommandText
$numberToSkipParameter = $selectCmd.Parameters.Add("@numberToSkip", $numberToSkip)
$selectCmd.Parameters.Add("@batchSize", $batchSize) | Out-Null

$dataAdapter = New-Object System.Data.SqlClient.SqlDataAdapter
$dataAdapter.SelectCommand = $selectCmd
$ds = New-Object System.Data.DataSet

function AddRowValue($p, $r, [string]$rn, [string]$tn)
{
    if ([string]::IsNullOrEmpty($tn))
    {
        $tn = $rn.Substring(0, 1).ToLowerInvariant() + $rn.Substring(1)
    }
    $p.Add($tn, $r[$rn]) | Out-Null
}

$rowsProcessed = 0

try
{

    $rowsAffected = $dataAdapter.Fill($ds, $mongoCollectionName)
    #"{0} row(s) selected" -f $rowsAffected
    while ($ds.Tables["Products"].Rows.Count -gt 0)
    {
        #$rowsAffected = $dataAdapter.Fill($ds, $mongoCollectionName)
        #"{0} row(s) selected" -f $rowsAffected
        $collection = $database.GetCollection($mongoCollectionName);
        $products = New-Object System.Collections.Generic.List``1[MongoDB.Bson.BsonDocument]
        foreach ($row in $ds.Tables["Products"].Rows)
        {
            $product = New-Object MongoDB.Bson.BsonDocument

            AddRowValue -p $product -r $row -rn "ProductId" -tn "_id"
            AddRowValue -p $product -r $row -rn "Name"
            AddRowValue -p $product -r $row -rn "ProductNumber"

            if (!$row.IsNull("Color"))
            {
                AddRowValue -p $product -r $row -rn "Color"
            }
        
            AddRowValue -p $product -r $row -rn "ListPrice"

            if ((!$row.IsNull("Size")) -and (!$row.IsNull("SizeUnitMeasureCode")))
            {
                $size = New-Object MongoDB.Bson.BsonDocument
                AddRowValue -p $size -r $row -rn "Size" -tn "value"
                AddRowValue -p $size -r $row -rn "SizeUnitMeasureCode" -tn "unitOfMeasure"
                $product.Add("size", $size) | Out-Null
            }

            if ((!$row.IsNull("Weight")) -and (!$row.IsNull("WeightUnitMeasureCode")))
            {
                $weight = New-Object MongoDB.Bson.BsonDocument
                AddRowValue -p $weight -r $row -rn "Weight" -tn "value"
                AddRowValue -p $weight -r $row -rn "WeightUnitMeasureCode" -tn "unitOfMeasure"
                $product.Add("weight", $weight) | Out-Null
            }

            if (!$row.IsNull("Class"))
            {
                AddRowValue -p $product -r $row -rn "Class"
            }

            if (!$row.IsNull("Style"))
            {
                AddRowValue -p $product -r $row -rn "Style"
            }

            $category = New-Object MongoDB.Bson.BsonDocument
            AddRowValue -p $category -r $row -rn "ProductCategoryId" -tn "categoryId"
            AddRowValue -p $category -r $row -rn "ProductCategoryName" -tn "name"

            $subCategory = New-Object MongoDB.Bson.BsonDocument
            AddRowValue -p $subCategory -r $row -rn "ProductSubcategoryId" -tn "subcategoryId"
            AddRowValue -p $subCategory -r $row -rn "ProductSubcategoryName" -tn "name"
            $category.Add("subcategory", $subcategory) | Out-Null
            $product.Add("category", $category) | Out-Null

            $products.Add($product)
        }

        $safeResult = $collection.InsertBatch([MongoDB.Bson.BsonDocument], $products, [MongoDB.Driver.WriteConcern]::Acknowledged)
        
        if ($safeResult.Ok)
        {
            $rowsProcessed = $rowsProcessed + $ds.Tables["Products"].Rows.Count
        }

        $numberToSkip = $numberToSkip + $batchSize
        $numberToSkipParameter.Value = $numberToSkip
        $ds.Clear()
        $rowsAffected = $dataAdapter.Fill($ds, $mongoCollectionName)
        #"{0} row(s) selected" -f $rowsAffected
    }

    Write-Host ([System.String]::Format("{0} products processed successfully.", $rowsProcessed)) -ForegroundColor Green
    Write-Host "Building subcategoryId index..." -ForegroundColor Yellow
    #$collection.EnsureIndex("subcategoryId")
    $collection.EnsureIndex("category.subcategory.subcategoryId")
    Write-Host "Index created successfully." -ForegroundColor Green
}
catch [System.Data.SqlClient.SqlException]
{
    Write-Error ([System.String]::Format("Error filling DataSet: {0}", $_.Exception.Message))
}
Write-Host "Product extract complete." -ForegroundColor Green
